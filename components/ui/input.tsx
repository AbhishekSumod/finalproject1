import * as React from "react";
import { cn } from "@/lib/utils";

export interface InputProps
  extends React.InputHTMLAttributes<HTMLInputElement> {}

const Input = React.forwardRef<HTMLInputElement, InputProps>(
  ({ className, type, ...props }, ref) => {
    return (
      <input
        type={type}
        className={cn(
          "flex h-12 w-full rounded-xl border border-neutral-800 bg-neutral-900 text-neutral-100 placeholder:text-neutral-400 shadow-md focus:ring-2 focus:ring-neutral-300 dark:bg-neutral-950 dark:text-neutral-50 dark:placeholder:text-neutral-500 transition-all duration-300",
          className
        )}
        ref={ref}
        {...props}
      />
    );
  }
);
Input.displayName = "Input";

export { Input };
